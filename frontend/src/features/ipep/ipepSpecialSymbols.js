export const IPEP_SPECIAL_SYMBOL_PAGES = rawSymbolPages.reduce((pages, page) => {
    const seen = new Set(pages.flat());
    const symbols = page.filter((symbol) => {
        if (seen.has(symbol)) return false;
        seen.add(symbol);
        return true;
    });
    if (symbols.length > 0) pages.push(symbols);
    return pages;
}, []);
